                <td width="100%" style="border: 1px solid #eee; padding-left:10px; padding-right:20px; color:#eee; font-size:14px;"><u><p align="center"><u><span class="Stil6">Wie schütze ich mich?</span></u></p>
                  </u>
                  <div align="center">
                    <p><br>
                      <span class="Stil7">Eine große Anzahl an Verteidigungsanlagen schreckt nicht jeden Spieler ab. Im Gegenteil. Einige Spieler werden durch eine große Anzahl von Verteidigungsanlagen sogar angelockt. In den so genannten Deff-ins-Trümmerfeld Unis ist es erst recht nicht ratsam eine große Anzahl an Verteidigungsanlagen zu bauen, da diese nun bei der Zerstörung in das Trümmerfeld   übergehen. <br>
                      </span><br>
                          <span class="Stil7">Hinzu kommt auch noch, dass es Interplanetar Raketen gibt. Diese Raketen sind einzig und alleine auf die Zerstörung von Verteidigungsanlagen ausgerichtet. Mit einem gut gezielten Angriff mit dieses Raketen   kann die Gefahr für die eigene Flotte stark vermindern. </span><br>
                      <br>
                          <span class="Stil7">Auch eine große Flotte hält nur Spieler ab, deren Flotte zu schwach ist um. Spieler die eine stärkere Flotte haben als du werden allerdings gerne vorbei kommen um deine Flotte zu vernichten und das entstehende   Trümmerfeld einzusacken. <br>
                            <br>
                            
                            <br>
                            Die wohl beste Möglichkeit seine Schiffe und Ressourcen zu schützen besteht darin, dass du deine Flotte + die gesamten Ressourcen auf deinem Planeten zu einem anderen Schickst. Angriffe im freien Flug sind nämlich   nicht möglich. </span><span class="Stil5"><br>
                              <br>
                    <u><span class="Stil6">Saven ist die beste Deff</span></u></span></p>
                    <p><span class="Stil5"><br>
                      </span><span class="Stil7">Das s.g. Saven ist einer der wichtigsten Bestandteile von XNOVA. Leider beherrschen   viele Spieler das Saven nicht. Aus diesem Grund hier eine Erklärung: Es gibt mehrere Möglichkeiten seine Schiffe und Ressourcen vor Übergriffen Feindlicher Flotten zu schützen. Dabei sind einige sehr sicher und andere beinhalten Risiken. </span>
                      </p>
                      </p>
                  </div>
                  <ul>
                    <li>Die erste Methode ist das Stationieren-Rückruf Saven. Hierbei schickt der Spieler seine Flotte auf einen seiner Planeten mit dem Auftrag Stationieren. Nach einiger Zeit bricht der Spieler den Flug ab, da der Rückflug einer Stationierung in der Ortung der Sensorphalanx nicht mehr sichtbar ist. Diese Methode ist nicht zu 100% sicher, da Gegner aus deiner Vergangenen Flugzeit (mit der Sensorphalanx zu erfahren) die Zeit errechnen können die deine Flotte für den Rückflug benötigt.</li>
                  </ul>
                  <ul>
                    <li>Eine der wohl am meisten Genutzten   Möglichkeiten ist der Flug von einem Mond oder Planeten zu einem Trümmerfeld. Hierbei ist der Flug von einem Mond auf das Trümmerfeld wesentlich sicherer, da Monde nicht per Sensorphalanx gescannt werden können. Es gibt allerdings ist auch diese Möglichkeit nicht zu 100% sicher, da Spieler die Trümmerfelder überwachen können. Aus der Zeit des Abbaus und der Entfernung   zu deinem Planeten können sie die ungefähre Flugzeit ermitteln, die deine Flotte noch unterwegs ist. Eine Möglichkeit   sich hiervor zu schützen ist das versenden von s.g. Geisterrecyclern. Diese werden einige Zeit vor der normalen Flotte ausgesendet und so verschwindet das Trümmerfeld schon bevor die eigentliche Flotte angekommen ist. Eine weitere Methode besteht darin, so kleine Trümmerfelder   zu schießen, dass diese nicht in der Galaxieansicht   auftauchen. Dies ist mit Spionagesonden   möglich. In der Galaxieansicht erscheint kein Trümmerfeld, doch für den der weiß dass dort Spionagesonden vernichtet wurden bietet dieses unsichtbare Trümmerfeld   eine ideale Save Möglichkeit. </li>
                  </ul>
                  <ul>
                    <li>Die Methode die wohl am sichersten ist, ist das Stationieren von Mond zu Mond. Dieser Flug kann nicht per Sensorphalanx geortet werden und auch das beobachten von Trümmerfeldern ist hier nutzlos. Die einzige   Möglichkeit diesen Flug abzufangen besteht darin, einen der Monde zu zerstören. Was  bei großen Monden allerdings sehr sehr Teuer und unwahrscheinlich ist. </li>
                  </ul></td>
